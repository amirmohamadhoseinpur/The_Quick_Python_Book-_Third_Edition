'''Decide which of the following expressions
can be a dictionary key: 1; 'bob'; ('tom', [1, 2, 3]); ["file-
name"]; "filename"; ("filename", "extension")'''

1                             # It can be a dictionary key
'bob'                         # It can be a dictionary key
('tom', [1, 2, 3])            # It can't be a dictionary key
["file-name"]                 # It can't be a dictionary key
"filename"                    # It can be a dictionary key
("filename", "extension")     # It can be a dictionary key